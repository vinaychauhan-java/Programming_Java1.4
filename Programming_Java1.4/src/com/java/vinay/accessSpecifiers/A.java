package com.java.vinay.accessSpecifiers;

public class A
{

	void getMessage1()
	{
		System.out.println("From Message - 1");
	}

	private void getMessage2()
	{
		System.out.println("From Message - 2");
	}

	protected void getMessage3()
	{
		System.out.println("From Message - 3");
	}

	public void getMessage4()
	{
		System.out.println("From Message - 4");
	}
}